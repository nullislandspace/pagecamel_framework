sub new {
    my ($proto, %config) = @_;
    my $class = ref($proto) || $proto;

    my $self = $class->SUPER::new(%config); # Call parent NEW
    bless $self, $class; # Re-bless with our class

    $self->{blocklist} = $self->getBlockedUrls();

    return $self;
}

sub register {
    my $self = shift;
    $self->register_logend("logend");
    return;
}

sub logend {
    my ($self, $ua, $header, $result) = @_;

    my $webpath = $ua->{url} || '--unknown--';
    my $dbh = $self->{server}->{modules}->{$self->{db}};

    if($result->{status} == 404 && contains($webpath, $self->{blocklist})) {
        my $insth = $dbh->prepare_cached("INSERT INTO accesslog_blocklist (ip_address, is_automatic, is_badbot, description)
                                         VALUES (?, true, false, ?)")
                or croak($dbh->errstr);
        if($insth->execute($ua->{remote_addr}, "Bad Path $webpath")) {
            $dbh->commit;
        } else {
            $dbh->rollback;
        }
    }


    return;
}


