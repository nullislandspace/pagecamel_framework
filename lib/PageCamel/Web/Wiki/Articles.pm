sub new {
    my ($proto, %config) = @_;
    my $class = ref($proto) || $proto;

    my $self = $class->SUPER::new(%config); # Call parent NEW
    bless $self, $class; # Re-bless with our class

    if(!defined($self->{orderby})) {
        croak("orderby must be defined in " . $self->{modname});
    }

    return $self;
}

sub register {
    my $self = shift;

