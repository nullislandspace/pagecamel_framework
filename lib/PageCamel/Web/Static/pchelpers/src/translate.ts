export function translateString(translate: string): string {
    var translations = (window as any).maskconfig.translations;
    for(var i = 0; i < translations.length; i++) {
        if(translations[i].key == translate) {
            //console.log("#########################################################################################");
            //console.log(translations[i]);
            return translations[i].value;
        }
    }

    //console.log("??????????????????????????????????????????????????????????????????????????????");
    //console.log(translate);
    return translate;
}
